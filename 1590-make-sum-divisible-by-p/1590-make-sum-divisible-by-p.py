class Solution:
    def minSubarray(self, nums: List[int], p: int) -> int:
        n = len(nums)
        total_sum = sum(nums)
        target_mod = total_sum % p
        
        if target_mod == 0:
            return 0
        
        # SÅ‚ownik: {reszta_z_dzielenia: ostatni_widziany_indeks}
        mod_map = {0: -1}
        
        current_sum = 0
        min_len = n 
        
        for i, num in enumerate(nums):
            current_sum += num
            current_mod = current_sum % p
            
            needed_mod = (current_mod - target_mod) % p
            
            if needed_mod in mod_map:
                found_len = i - mod_map[needed_mod]
                min_len = min(min_len, found_len)
            
            mod_map[current_mod] = i
            
        return min_len if min_len < n else -1